public class OrderBean {
    private String oid , ordertime,total,state,uid,address;
//订单id 订单生成时间 总价 状态 用户id 用户地址
    @Override
    public String toString() {
        return "OrderBean{" +
                "oid='" + oid + '\'' +
                ", ordertime='" + ordertime + '\'' +
                ", total='" + total + '\'' +
                ", state='" + state + '\'' +
                ", uid='" + uid + '\'' +
                ", address='" + address + '\'' +
                '}';
    }

    public OrderBean() {
    }

    public OrderBean(String oid, String ordertime, String total, String state, String uid, String address) {

        this.oid = oid;
        this.ordertime = ordertime;
        this.total = total;
        this.state = state;
        this.uid = uid;
        this.address = address;
    }

    public String getOid() {

        return oid;
    }

    public void setOid(String oid) {
        this.oid = oid;
    }

    public String getOrdertime() {
        return ordertime;
    }

    public void setOrdertime(String ordertime) {
        this.ordertime = ordertime;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
