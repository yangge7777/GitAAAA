@WebFilter(filterName = "CartFilter",urlPatterns ="/cartServlet")
public class CartFilter implements Filter {
    public void destroy() {
    }

    public void doFilter(ServletRequest req, ServletResponse resp, FilterChain chain) throws ServletException, IOException {
        HttpServletRequest request = (HttpServletRequest) req;
        HttpServletResponse response= (HttpServletResponse) resp;
        HttpSession session =request.getSession();
      String uid = (String) session.getAttribute("uid");
        if (uid==null||uid.isEmpty()){
            request.setAttribute("msg","登录之后购买");
          //  response.getWriter().write("<script>alert(\"请先登录\")</script>");
            request.getRequestDispatcher("/jsps/msg.jsp").forward(request,response);
        }else {
            chain.doFilter(request,response);
        }



    }

    public void init(FilterConfig config) throws ServletException {

    }

}
